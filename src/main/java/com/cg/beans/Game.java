package com.cg.beans;

public class Game {
	private String name;
	private byte no_of_player;
		
	public Game() {
		super();
	}


	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public byte getNo_of_days() {
		return no_of_player;
	}
	public void setNo_of_days(byte no_of_days) {
		this.no_of_player = no_of_days;
	}


	
}
